# List containing numbers as strings
str_list = ['10', '2', '5', '1', '20']

# List containing numbers as integers
int_list = [10, 2, 5, 1, 20]

# Sort both lists
str_list_sorted = sorted(str_list)
int_list_sorted = sorted(int_list)

# Print sorted lists
print("Sorted str_list:", str_list_sorted)
print("Sorted int_list:", int_list_sorted)
