# Assign the integer value 68 to variable a
a = 68

# Convert the integer value 'a' to its corresponding character and print it
print(f"Character corresponding to ASCII code {a}: {chr(a)}")

# Assign the character 't' to variable b
b = 't'

# Obtain the ASCII code of the character 'b' and print it
print(f"ASCII code of character '{b}': {ord(b)}")
