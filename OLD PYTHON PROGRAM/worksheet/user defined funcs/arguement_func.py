# Define a function named 'sumdata' that takes two parameters, 'num1' and 'num2'.
def sumdata(num1, num2):
    # Print the sum of 'num1' and 'num2'.
    print(num1 + num2)

    # Print the result of 'num1' divided by 'num2'.
    print(num1 / num2)

    # Print the remainder when 'num1' is divided by 'num2'.
    print(num1 % num2)

    # Print the product of 'num1' and 'num2'.
    print(num1 * num2)

    # Print the result of subtracting 'num2' from 'num1'.
    print(num1 - num2)

    # Print 'num1' raised to the power of 'num2'.
    print(num1 ** num2)


# Assign values to 'num1' and 'num2'.
num1 = 12
num2 = 324

# Call the 'sumdata' function with the assigned values of 'num1' and 'num2'.
sumdata(num1, num2)
