# Create a list containing various data types
my_list = [1, "apple", True, 3.14]

# Print the entire list
print("my_list:-", my_list) # Output: my_list:- [1, 'apple', True, 3.14]

# Print the list reversed, starting from the second element (inclusive)
# [2::-1] signifies:
# - Start from index 2 (inclusive)
# - Include all elements
# - Reverse the order
print("my_list:-", my_list[2::-1]) # Output: my_list:- [True, 'apple', 1]
